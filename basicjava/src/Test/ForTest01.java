package Test;
public class ForTest01 {
	public static void main(String[] args) {
	for(int i=1;i<=10;i++) {
		System.out.println("자바프로그래밍");
	    }
	System.out.println("===========================");
	int count = 1;
	for(int i=10;i<=14;i++) {
		System.out.println("자바프로그래밍"+count);
		count++;
	    }
	System.out.println("===========================");
	for(int i=5;i>=1;i--) {
		System.out.println("자바프로그래밍");
	    }
	}
}
