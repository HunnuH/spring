package Test;

public class APITest02 {
public static void main(String[] args ) {
	 String str = new String("java");
//string 클래스에 정의되어 있는 charAt메소드를 사용
/* string클래스의 charAt메소드는 매개변수가 1개이고 매개변수의 타입이 int charAt을 사용하기
 * 위해서 ()안에 int형 숫자를 정의한다.
 * charAt의 실행결과가 char타입이므로 실행 결과를 내 프로그램 안에서 활용하기 위해서 char타입의 변수를
 * 선업해서 결과를 저장
 */
      char result = str.charAt(3);
      System.out.println("3번위치의문자는"+result);
}
}
